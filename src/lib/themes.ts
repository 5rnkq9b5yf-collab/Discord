export const DEFAULT_THEME_ID = "pastel-blue";

export function getTheme(id: string): Theme {
  return THEMES.find((t) => t.id === id) ?? THEMES[0];
}

export function applyTheme(tokens: Theme["tokens"]): void {
  const r = document.documentElement;
  r.style.setProperty("--lyra-primary-bg", tokens.primaryBg);
  r.style.setProperty("--lyra-secondary-bg", tokens.secondaryBg);
  r.style.setProperty("--lyra-tertiary-bg", tokens.tertiaryBg);
  r.style.setProperty("--lyra-sidebar-bg", tokens.sidebarBg);
  r.style.setProperty("--lyra-channel-sidebar-bg", tokens.channelSidebarBg);
  r.style.setProperty("--lyra-chat-bg", tokens.chatBg);
  r.style.setProperty("--lyra-message-bg", tokens.messageBg);
  r.style.setProperty("--lyra-accent", tokens.accent);
  r.style.setProperty("--lyra-accent-hover", tokens.accentHover);
  r.style.setProperty("--lyra-accent-glow", tokens.accentGlow);
  r.style.setProperty("--lyra-text-primary", tokens.textPrimary);
  r.style.setProperty("--lyra-text-secondary", tokens.textSecondary);
  r.style.setProperty("--lyra-text-muted", tokens.textMuted);
  r.style.setProperty("--lyra-border", tokens.border);
  r.style.setProperty("--lyra-border-glass", tokens.borderGlass);
  r.style.setProperty("--lyra-border-glow", tokens.borderGlow);
  r.style.setProperty("--lyra-glass-sidebar", tokens.glassSidebar);
  r.style.setProperty("--lyra-glass-card", tokens.glassCard);
  r.style.setProperty("--lyra-glass-modal", tokens.glassModal);
  r.style.setProperty("--lyra-glass-input", tokens.glassInput);
  r.style.setProperty("--lyra-glass-hover", tokens.glassHover);
  r.style.setProperty("--lyra-glass-active", tokens.glassActive);
  r.style.setProperty("--lyra-button-primary", tokens.buttonPrimary);
  r.style.setProperty("--lyra-button-primary-text", tokens.buttonPrimaryText);
  r.style.setProperty("--lyra-input-bg", tokens.inputBg);
  r.style.setProperty("--lyra-input-border", tokens.inputBorder);
  r.style.setProperty("--lyra-status-online", tokens.statusOnline);
  r.style.setProperty("--lyra-status-idle", tokens.statusIdle);
  r.style.setProperty("--lyra-status-dnd", tokens.statusDnd);
  r.style.setProperty("--lyra-status-offline", tokens.statusOffline);
  r.style.setProperty("--lyra-scrollbar", tokens.scrollbar);
  r.style.setProperty("--lyra-scrollbar-hover", tokens.scrollbarHover);
  // Update body background gradient to match theme primary
  document.body.style.backgroundColor = tokens.primaryBg;
}
